#include <stdio.h>

/** \brief Toma un numero entero.
 *
 * \param [] char Texto que especifica cual numero estoy tomando.
 * \return int El numero ingresado.
 *
 */
int IngresarEntero(char []);

/** \brief La suma de dos numeros enteros.
 *
 * \param int El primer numero.
 * \param int El numero a sumar.
 * \return int El resultado de la suma.
 *
 */
int SumarNumeros(int ,int);

/** \brief La resta de dos numeros enteros.
 *
 * \param int El primer numero.
 * \param int El numero a restar.
 * \return int El resultado de la resta.
 *
 */
int RestarNumeros(int ,int);

/** \brief La division de dos numero y su validacion.
 *
 * \param numeroDividendo int El numero a dividir.
 * \param divisor int El divisor.
 * \return int Regresa el resultado en un char.
 *
 */
float SacarDivision(int numeroDividendo ,int divisor);

/** \brief La operacion para multiplicar dos numeros.
 *
 * \param int El numero a multiplicar.
 * \param int El multiplicador.
 * \return int Regresa el resultado de la multiplicacion.
 *
 */
int SacarMultiplicacion(int ,int);

/** \brief Sacar el factorial de un numero.
 *
 * \param int El numero al que hay que factorear.
 * \return int Regresa el resultado del factoreo.
 *
 */
int SacarFactorial(int);
